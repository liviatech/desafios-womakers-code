''' Faça um Programa que peça dois números, realize as principais
operações soma, subtração, multiplicação, divisão'''

numero1 = float(input('Digite um numero : '))
numero2 = float(input('Digite um numero : '))

soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao =  numero1 / numero2
print(f'O resultado da soma é : {soma}, da subtração é : {subtracao}, da multiplicação é : {multiplicacao}, da divisão é : {divisao} .')
